#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*str;

	str = (char *)s + ft_strlen(s);
	while (str >= s)
	{
		if (*(str) == (char)c)
			return (str);
		str--;
	}
	return ((void *)0);
}
